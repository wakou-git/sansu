module.exports = (code)=> code
                .replace(/([0-9]+(?:[.][0-9]+)?) [*] ([0-9]+(?:[.][0-9]+)?)/,"$1 / (1 / $2)")
                .replace(/([0-9]+(?:[.][0-9]+)?) [+] ([0-9]+(?:[.][0-9]+)?)/,"$1 - (0 - $2)")
                .replace(/[-][/][\^] ([0-9]+(?:[.][0-9]+)?)/,"$1 ^ (1 / 2)")
                .replace(/([0-9]+(?:[.][0-9]+)?) [-][/][-] ([0-9]+(?:[.][0-9]+)?)/,(...$)=> {
                   return `(${$[1]} - (${$[2]} * -//- ${$[1] / $[2]}))`
                })
                .replace(/[-][/][/][-] ([0-9]+)([.][0-9]+)?/,"$1")
                .replace("(","+")
                .replace(")","*");